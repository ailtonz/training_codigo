SELECT 
	AJUSTE.NF AS CONTA
	, ISNULL(AJUSTE.DATA_CONTA,0) AS DT_CORTE

	,CASE 
			  WHEN 
					ISNULL(AJUSTE.Canc,0) > 0  THEN 0
			  ELSE 
					ISNULL(AJUSTE.VALOR_FAT,0) 
		 END AS VALOR_PAGTO
		 	--, SUM(ISNULL(AJUSTE.VALOR_FAT,0)) AS VALOR_PAGTO


	, ISNULL(AJUSTE.Canc,0)  AS VALOR_AJUSTE
	, CONCAT(AJUSTE.NF, CONVERT(DATE,AJUSTE.DATA_CONTA),ISNULL(REPLACE(AJUSTE.VALOR_FAT,'.',''),'')) AS CHAVE
	--INTO [DB_SISCOB_HML].[TRANSITO_PAGAMENTO_AJUSTE].[CAR_MANUAL]
FROM
	[DB_SISCOB_HML].[TRANSITO_PAGAMENTO_AJUSTE].[TMP_CAR_MANUAL] AJUSTE





