SELECT 
	 W.RAIZ
	,W.AREA_ATUACAO
	,SUM(W.SALDO_CAR_26) SALDO_CAR_26
  FROM 
  					[DB_SISCOB].[APP_EXCEL].[VW_CONSUMO_CONSOLIDADO_ANALISTA] W
		INNER JOIN  [DB_SISCOB].[dbo].[RAIZES_VPG]							  V ON W.RAIZ = V.RAIZ	

  WHERE
		SEG_DESCR LIKE 'VPG%'

	GROUP BY

		 W.AREA_ATUACAO
		,W.RAIZ

	ORDER BY
		
		RAIZ 